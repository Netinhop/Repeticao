
package Repeticao;
public class repeticao {

    public static void main(String[] args) {
      int cc = 0;
      //cc = Monstrara numero de repetição
      //Camalhota = Cambalhota o tanto de repitação ate o numero estimado
      do{
          System.out.println(cc);
          cc++;
      } while (cc<4);
    }
    
}
